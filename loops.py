# A while loop
number = 20
while number <= 25:
    print(number)
    number += 1

# Decrementing value
a = 10
while a >= 1:
    print(a)
    a -= 1

# For loop
languages = ["Python", "PHP", "Kotlin", "C++"]
for y in languages:
    print(y)

# Range
for x in range(10):
    print(x)

for z in range(15, 20):
    print(z)

for b in range(4, 30, 2):
    print(b)